package domain;

/**
 *
 * @author pc
 */
public class admin {
    private  int id;
     private  String name , pass; 
     
    public admin(int id, String name, String pass) {
        this.id = id;
        this.name = name;
        this.pass = pass;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getName() {
        return name;
    }

    public String getPass() {
        return pass;
    }

   
    
}
